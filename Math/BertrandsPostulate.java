package test.math;

import java.util.Arrays;
import java.util.Scanner;

public class BertrandsPostulate {

	public boolean[] prime;
	
	BertrandsPostulate(){
		//미리 에라토스테네스의 체 를 이용 해서 소수여부를 구한다.
		prime = new boolean[123456*2 + 1];	//전체 소수여부를 구한다.
		Arrays.fill(prime, true);
		prime[0] =prime[1] = false;	//0과 1은 소수가 아니다.
		
		for(int i = 2; (i*i) <= prime.length; i++) {
			if(prime[i]) {
				for(int j = i*i; j <= prime.length ;j += i) {
					prime[j] = false;
				}
			}
		}
	}
	
	public int getPrimeNum(int n){
		int result = 0;
		for(int i = n+1 ; i <= 2*n ; i++) {
			if(prime[i]) result++;
		}
		return result;
	}
	
	public static void main(String[] args) {
		//스캐너로 하자.
		Scanner sc = new Scanner(System.in);
		BertrandsPostulate mn = new BertrandsPostulate();
		int num = sc.nextInt();
		while(num != 0) {
			System.out.println(mn.getPrimeNum(num));
			num = sc.nextInt();
		}
		sc.close();
	}
}
